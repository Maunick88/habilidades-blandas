		<h3>1. Adaptabilidad&nbsp;</h3>
			<p><strong>Tipo de habilidad blanda: </strong>actitudinal.</p>
			<p>Una de las habilidades blandas más buscadas por las empresas es la adaptabilidad, la cual significa ser flexible y capaz de cambiar para lograr el éxito. Esta cualidad es de gran importancia, pues muchos roles requieren del manejo de circunstancias inusuales donde no hay una guía establecida sobre cómo se debe reaccionar.</p>
			<p>Por todo lo anterior, la adaptabilidad permite que un colaborador sepa cómo responder eficazmente. Esta habilidad blanda también se relaciona con la confianza que tenga la persona en sí misma sobre sus decisiones, por más difíciles que sean.</p>
		<h4><strong>Ejemplos de aplicación de la adaptabilidad:</strong></h4>
		<ul>
			<li>Cuando una empresa hace un cambio de plataforma para la gestión de la información interna, debe existir adaptabilidad para que los empleados modifiquen sus hábitos y adopten nuevas competencias para trabajar con el nuevo sistema.&nbsp;</li>
			<li>Durante 2020, cuando las empresas migraron a los formatos de trabajo remotos, quienes mejor lograron adaptarse fueron quienes mantuvieron un buen papel laboral o un desempeño empresarial progresivo.&nbsp;</li>
		</ul>
		<h3>2.&nbsp;Pensamiento creativo</h3>
			<p><strong>Tipo de habilidad blanda: </strong>reflexiva.</p>
			<p>A diferencia del pensamiento crítico, el pensamiento creativo está más centrado en la innovación y la capacidad de una persona para aportar nuevas ideas en su lugar de trabajo. No significa que sean opuestos; en realidad vendrían a ser complementarios. Esto puede ir desde tener otra perspectiva sobre un problema hasta dar una resolución, un nuevo resultado o algún enfoque a un método antes no probado.</p>
			<p>Por ejemplo, si una empresa requiere del aporte de nuevas características para un producto, un colaborador con pensamiento creativo podría ver un lado que nadie más esté percibiendo&nbsp;y, con base en ello, aportar una idea novedosa que antes no se había explorado. Al pensamiento creativo también se le conoce como «Pensar fuera de la caja», lo que es ver más allá de lo obvio. Esta habilidad blanda también sirve cuando se trata de resolver problemas o enfrentar nuevos desafíos.</p>
		<h4><strong>Ejemplos de aplicación del pensamiento creativo:</strong></h4>
		<ul>
			<li>Si quieres generar una campaña publicitaria única, es necesario poner en práctica el pensamiento creativo. Gracias a esta habilidad, puedes llegar a respuestas innovadoras y únicas con base en lo que ya se ha hecho en otros momentos o por parte de tu competencia.&nbsp;</li>
			<li>El pensamiento creativo es muy común entre los emprendedores, quienes para tener éxito en el mercado tienen que ofrecer productos nuevos y que ponen en cuestionamiento los que ya están establecidos (piensa en el caso de Netflix o de Uber, que revolucionaron con un servicio único gracias al pensamiento creativo).&nbsp;</li>
		</ul>
		<h3>3. Trabajo en equipo</h3>
			<p><strong>Tipo de habilidad blanda:</strong> interpersonal.</p>
			<p>Seguro en todas las ofertas laborales encontrarás que solicitan a una persona con habilidades para trabajar en equipo. Esta también es una habilidad blanda relevante, pues más allá de tu función, es vital que sepas colaborar con los demás y seas capaz de transmitir tus fortalezas e ideas con los miembros de tu equipo.</p>
			<p>Contar con esta habilidad blanda es tener una buena comunicación y una actitud positiva y servicial para ayudar a aquellos que lo necesiten. Esto también puede ir por el lado contrario, cuando es necesario que tú seas el que escuche alguna retroalimentación. Aquellas personas que tienen esta cualidad demuestran más capacidad de liderazgo y colaboración, sin importar que su rol requiera de actividades individuales.</p>
		<h4><strong>Ejemplos de aplicación del trabajo en equipo:</strong></h4>
		<ul>
			<li>Esta habilidad se da en el plano más cotidiano, cuando las personas tienen que ponerse de acuerdo para llegar a una meta dividiendo las tareas de forma inteligente y con base en las cualidades de cada miembro del equipo.</li>
			<li>Si, por ejemplo, te has retrasado en una entrega y uno de tus compañeros de trabajo decide apoyarte para que ambos terminen a tiempo, este miembro del equipo está haciendo uso de sus habilidades para trabajar en equipo.&nbsp;</li>
		</ul>
