		<h3>4. Ética laboral&nbsp;</h3>
			<p><strong>Tipo de habilidad blanda:</strong> actitudinal.</p>
			<p>La ética laboral se refiere a ser coherente con tus ideas y acciones, algo que las empresas valoran mucho de sus colaboradores. Esta habilidad es interpersonal, pues con el tiempo vas aprendiendo a apreciar y comprometerte con aquello importante para ti. Esta cualidad genera credibilidad, lo que hace que la organización a la que pertenezcas te brinde un voto de confianza, crea en tus decisiones y en lo que dices y haces.</p>
			<p>Cuando alguien es ético en su lugar de trabajo también es menos conflictivo y muestra más compromiso con lo que hace. Alguien que se desenvuelve con sinceridad y valores siempre busca lo mejor para sí mismo, pero también para el bien común.</p>
		<h4><strong>Ejemplos de aplicación de la ética laboral:</strong></h4>
		<ul>
			<li>Esta habilidad se hace presente cuando, por ejemplo, un empleado pone a la misión de la empresa como una de sus guías a la hora de contactar al cliente.</li>
			<li>La ética laboral también se concreta en actos que refuerzan la imagen de profesionalismo de un líder; por ejemplo, cuando delega responsabilidades de manera objetiva y sin sesgos de género o de edad.&nbsp;</li>
		</ul>
		<h3>5. Pensamiento estratégico</h3>
			<p><strong>Tipo de habilidad blanda: </strong>reflexiva.</p>
			<p>El pensamiento estratégico es un conjunto de factores que favorecen esta cualidad, entre ellos la función de planeamiento y control, así como lo actitud de apertura mental, de reflexión y participación en la toma de decisiones.</p>
			<p>Esta habilidad blanda es buscada por los reclutadores, sobre todo para aquellos roles donde se requiere una persona que pueda responder a las circunstancias cambiantes. Va muy de la mano con el liderazgo, pues fomenta la planeación y la comunicación, pero también la capacidad de enfrentarse a problemas de diferente índole, ver oportunidades y detectar amenazas. &nbsp;</p>
		<h4><strong>Ejemplos de aplicación del pensamiento estratégico:</strong></h4>
		<ul>
			<li>El&nbsp;pensamiento estratégico ocurre, por ejemplo, cuando una líder divide un proceso en pasos para poner primero las tareas que son simples, mientras que las más pesadas se dejan al final.</li>
			<li>Otro buen ejemplo es cuando un emprendedor decide adquirir una deuda para financiar su proyecto de negocios y lleva a cabo una planeación de pago a largo plazo, considerando los mejores y peores escenarios financieros a los que se puede enfrentar.&nbsp;</li>
		</ul>
		<h3>6. Atención a los detalles</h3>
			<p><strong>Tipo de habilidad blanda: </strong>actitudinal.</p>
			<p>Esta habilidad blanda es la capacidad de percibir hasta el más mínimo elemento destacable dentro de grandes volúmenes de información. Esto permite procesar correctamente lo más relevante a alguna situación o propuesta.</p>
			<p>Cuando un reclutador busca a un colaborador con atención al detalle significa que quiere encontrar&nbsp;una persona que sea capaz de analizar a profundidad cualquier cosa para detectar oportunidades o incluso amenazas o errores. De esta manera, la empresa puede estar libre de equivocaciones fatales o muy obvias.</p>
		<h4><strong>Ejemplos de aplicación de la atención a los detalles:</strong></h4>
		<ul>
			<li>Esta habilidad es fundamental para el diseño de prototipos. En estos procesos lo más importante es asegurarse de que cada aspecto del producto final sea perfecto. Por ello, los gestores de desarrollo comercial deben contar con un ojo hábil para detectar errores o fallos.</li>
			<li>La atención al detalle también es fundamental en los departamentos legales, quienes tienen que cubrir cada aspecto de los contratos y de los acuerdos comerciales con el fin de que no haya lagunas o interpretaciones incorrectas de los documentos.</li>
		</ul>
