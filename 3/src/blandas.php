				<p><strong>Liderazgo:</strong> El liderazgo es la capacidad de influir, motivar y guiar a individuos o grupos hacia el logro de metas y objetivos comunes. Implica la habilidad de comunicar una visión clara, inspirar confianza, tomar decisiones efectivas, delegar responsabilidades, resolver conflictos y fomentar el desarrollo personal y profesional de los miembros del equipo. Un líder eficaz es capaz de adaptarse a diferentes situaciones y estilos de liderazgo según las necesidades del equipo y el contexto en el que opera.</p>

				<p><strong>Accountability:</strong> La accountability, o rendición de cuentas, es la responsabilidad de los individuos o grupos de rendir cuentas por sus acciones, decisiones y resultados ante ellos mismos, ante otros y ante las normas establecidas. Implica asumir la responsabilidad de los éxitos y fracasos, cumplir con los compromisos adquiridos, ser transparente en las acciones y decisiones, y aceptar las consecuencias de las acciones. La accountability es fundamental en la toma de decisiones éticas, el establecimiento de relaciones de confianza y la mejora continua en el ámbito personal y organizacional.</p>
