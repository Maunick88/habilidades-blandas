				<p><strong>Habilidades Comunicativas:</strong> Las habilidades comunicativas son aquellas capacidades que permiten a una persona transmitir información de manera efectiva, clara y comprensible, tanto verbalmente como por escrito. Incluyen la capacidad de expresarse con claridad, escuchar activamente, adaptar el mensaje al público objetivo, utilizar un lenguaje adecuado y no verbal, y resolver conflictos de manera constructiva. </p>
